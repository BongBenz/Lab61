
public class SubTotals {
	//Exercise 17
	public static void main(String[] args) {
		int sum;
		   sum = 0;
		   System.out.println("The value of sum is initially set to " + sum);
		   
		   sum = sum + 96;
		   System.out.println("  Sum is now "  + sum );
		   
		   sum = sum + 70;
		   System.out.println("  Sum is now "  + sum );
		   
		   sum = sum + 85;
		   System.out.println("  Sum is now "  + sum );
		   
		   sum = sum + 60;
		  System.out.println("The final sum is  "  + sum );
	}

}
